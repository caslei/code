%% 

%% load image
sig_orig = double(imread(im_name)); 
%% 

%% construct noisy image if necessary
if raw_denoising
    sig = sig_orig;
else
    noise = randn(size(sig_orig));
    corr_noise = imfilter(noise, em_corr_filter, 'symmetric');
    corr_noise = corr_noise / std2(corr_noise);
    sigma = sigma_0*ones(size(sig_orig)) + alpha*sig_orig;
    sig = sig_orig + sigma.*corr_noise;
end
%% 

%% denoising
tic;
[sig_cum, weights_cum] = fNLMS_SC(sig, sig, h, em_corr_filter_inv, alpha, sigma_0, W, wnd_size, weighting_function_name);
sig_est = sig_cum./weights_cum;
toc;
%% 

%% visualize results
if raw_denoising
    figure,imshow(sig,[]),title(sprintf('noisy image'));
    figure,imshow(sig_est,[]),title(sprintf('denoised image'));
else
