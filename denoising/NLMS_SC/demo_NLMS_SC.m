    disp(sprintf('noisy image PSNR=%f dB', PSNR(sig,sig_orig)));
    disp(sprintf('denoised image PSNR=%f dB', PSNR(sig_est,sig_orig)));
    figure,imshow(sig,[0,255]),title(sprintf('noisy image PSNR=%f dB', PSNR(sig,sig_orig)));
    figure,imshow(sig_est,[0,255]),title(sprintf('denoised image PSNR=%f dB', PSNR(sig_est,sig_orig)));
